$('#buttonWind').click(function(){
    $(this).toggleClass('on');
    $('#offWind').toggleClass('hide');
    $('#onWind').toggleClass('hide');
    $('#onTextWind').toggleClass('invis');
    $('#onHatWind').toggleClass('hide');
    $('#hatWind').toggleClass('hide');
});

